package com.Cintia.Pertemuan3.Praktikum.Agregasi;

public class Mahasiswa {
    private String nama;
    private int NIM;

    public Mahasiswa(String nama, int NIM) {
        this.nama = nama;
        this.NIM = NIM;
    }

    public String getNama() {
        return nama;
    }

    public int getNIM() {
        return NIM;
    }
}

